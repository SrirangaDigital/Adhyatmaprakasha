			<div class="title">A brief report on the Vedanta Vidvadgoshthi held at Adhyatma Prakasha Karyalaya, Holenarasipura, on March 5, 6, and 7, 2011.</div>
			<p>
				<b>Every day before 7.15 am:</b> Pooja to Digvijaya Ramachandra and at the Samadhi of Sri Sri Swami Satchidanandendra Saraswati was held.<br /><br />

The Goshthi was held from 7.30 am to about 6.30 pm on 5th and 6th, and from 7.30 am to 1 pm on 7th, with breaks for breakfast (9 am to 10 am) and lunch (1.30 pm to 3 pm). Tender coconut water, water melon juice, badam milk and water was served to all during the Goshthi itself. Dinner was served after 8 pm.<br /><br />

All the deliberations of the Goshthi were held using only Sanskrit language. Summary of discussions in Kannada language was given on all the three days after the Goshthi. About twenty Vidvans and Sanyasis participated in the Goshthi and around a hundred others witnessed it.<br /><br />

<b>On 05.03.11, Saturday: from 7.30 am:</b> Lighting of Lamp, Mangala Arati at Sri Sri Swami Satchidanandendra Saraswati’s Samadhi. General Welcome to Vidvans and all others and introducing the President Sri Swami Prakashanandendra Saraswati.<br /><br />

<b>Commencement of Goshthi :</b> The President initially set the rules to be followed during the discussion: presentation and discussion of topics to be done on the basis of prasthanatraya bhashyas and yukti alone; no digression to some other topic; should speak only as much as is necessary; essential meaning of the Bhashyas to be brought out etc. The President assured that everyone has full freedom to express personal opinion. However, opinion of others has to be respected and personal attacking, insulting etc., needs to be avoided. Those who present topics will take a maximum of twenty minutes, during which time no interjection by anyone. Later the topic will be discussed for a maximum of one hour. The president will summarise the discussion in ten minutes. The same procedure will be followed for all topics.<br /><br />

Sri Swami Advayanandendra Saraswathi who has guided all the activities related to the  Goshthi right from the beginning, over six months ago, said that the source of inspiration for all the activities of the Karyalaya and the present Goshthi is Sri Sri Swami Satchidanandendra Saraswati.<br /><br />

