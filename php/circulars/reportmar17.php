<p>
The two topics that were not discussed due to lack of time: Anvaya_vyatireka_lakṣaṇam – tarkasvarūpam and Māyā_svarūpam.<br /><br />

In general, the Goshthi was held in a cordial atmosphere. The music of the fluent Sanskrit spoken during discussions and other deliberations was a treat to the ears according to many.<br /><br />

A summary of the discussions on various topics would appear in the Commemoration Volume that will be published eventually. The following are some general observations:<br /><br />

It appears that the differences in the view points on several issues remain intact even after the Goshthi. Broadly, two differing viewpoints could be seen:<br /><br />

According to one view point moolavidya has to be taken as the cause of adhyasa; Then only it will be as per Bhashyas. The other view point is that as per Bhashyas, adhyasa is avidya and no cause for adhyasa should be posited. However, from both points of view, arriving at nishcaya jnana about Atmaswarupa is Vidya, and avidya is that which is removed by vidya. Differences in the interpretation of Bhshyas to support each view exist. The Goshthi witnessed lengthy discussions on interpreting Bhashya relating to ‘mahatah paramavyaktam’ and ‘avidya hi avyaktam’.<br /><br />

According to one view, if the cause of the world is taken as nirguna Brahman, it would not be proper. According to the other the Brahma Sutras say the opposite. According to both, however, the srushti prakriya is only to teach Atma-Brahma ekatva.<br /><br />

One view point is that adhyaropa-apavada prakriya should be applied only in relation to nirguna pratipadaka sruti vakyas. But according to the other view, the prakriya must be applied for all sruti vakyas.<br /><br />

There is avidya in sushupti. There is no avidya in sushupti;<br /><br />

ajnana is bhavarupa padartha. ajnana is jnana abhava; <br /><br />

Sakshi (Witness) cannot be considered to be a pramana. No, sakshi anubhava is very much there.<br /><br />

No prarabdha karma for jnani; the state of mukti is Brahman only (Brahmaiva hi muktyavasthaa); it is ajnanis prarabdha which makes them see jnani in body; sadyomukti or jivanmukti is the main mukti and this is at the same time of attaining samyakjnana/ avidyanivrutti; videhamukti is told only as courtesy (upacara); krama mukti is for upasakas; if avidya in sushupti is accepted then there will be no dristanta (illustration) for moksha; no avidyalesa for jnani; no prasankhyana (jnanabhyasa), etc., etc. These are according to one view. However these are not acceptable to others.<br /><br />

The concluding remarks at the end of discussions on each topic by Sri Swami Prakashanandendra Saraswathi were well balanced and were appreciated by all.<br /><br />

The Goshthi was concluded on 7th March by 1 pm. All the Vidvans were honoured with shawls, fruit basket and flowers. Honorariums and travel expenditure were also paid. The efforts of the Adhyatma Prakasha Karyalaya in holding the Vidvadgoshthi and the arrangements and hospitality were praised by many, without any reservations. Many hoped that such Vidvadgoshthis must happen frequently.<br /><br />

