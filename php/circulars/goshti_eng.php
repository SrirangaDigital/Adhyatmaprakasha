			<div class="title">Dakshina Bharata Shaankara Vedanta Vidvadgoshti - March 5th to 7th, 2011</div>
			<p>
				Adhyatma Prakasha Karyalaya is organising a unique three-day Conference on Shaankara
				Vedanta. The Conference will be held in the Karyalaya premises at Holenarasipura, on the above
				dates. All the Conference deliberations including presentations, discussions etc., will be held in
				the divine language Sanskrit. Eminent persons and Vidvans who are well versed in Shaankara
				Vedanta and who are capable of presenting and discussing various topics in Sanskrit have been
				extended invitation to participate in the Conference.<br /><br />
				
				Over thirty vidvans from the states of Maharastra, Andhra, Tamilnadu, Kerala and Karnataka are
				expected to participate. Some of the well-known advaitins who are expected to participate in the
				Conference are- R. Krishnamurthy Sastrigal, Mani Dravida Sastrigal, Swami Prakashanandendra
				Saraswathi, Swami Advayanandendra Saraswathi, K.G.Subbaraya Sharma, and Mattur
				Aswathanarayana Avadhani etc.<br /><br />
				
				The various topics chosen for the the Conference are given here for information. The objective is
				to bring out the essence of Shankara Bhashyas. Therefore Vidvans have been requested to
				present their topics and conduct discussions with references from prasthanatraya bhashyas of
				Shankara only and not other texts/vyakhyanas.<br /><br />
				
				A commemoration volume has been planned to be brought out. This volume will include the
				Essays in Sanskrit by their respective topics by the Vidvans, and also English Essays on the same
				topics by other invited advaitins.<br /><br />
				
				During late evenings on each day of the Conference, summary presentations in Kannada on the
				topics of that day will be provided for the benefit of jijnasus who know Kannada.<br /><br />
				
				Interested jijnasus are welcome to attend the Conference. They will be able to listen and watch
				the proceedings and receive general hospitality like lunch and coffee. However they have to
				make their own arrangements for accommodation etc. They may also note that the
				accommodation available in the Karyalaya premises and a few places outside would be reserved
				only for the invited Vidvans and other guests.<br /><br />
				
				For more information, contact the Karyalaya directly.</p>
				
