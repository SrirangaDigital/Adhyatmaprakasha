				<p>
				ಶಿಬಿರ : 	ತಾರೀಖು 09-10-2009 ರಿಂದ 15-10-2009 ರ ವರೆಗೆ  <br />
				ಸ್ಥಳ : 	ಮತ್ತೂರು, ಶಿವಮೊಗ್ಗ<br />
				ವಿಷಯ :	ಕೇನೋಪನಿಷತ್ತು ಮತ್ತು ದಕ್ಷಿಣಾಮೂರ್ತಿಸ್ತೋತ್ರದ ಮೇಲೆ ಉಪನ್ಯಾಸಗಳು<br />
				ಬೆಳಗ್ಗೆ: ಕೇನೋಪನಿಷತ್ತು<br />
				ಸಂಜೆ: ದಕ್ಷಿಣಾಮೂರ್ತಿ ಸ್ತೋತ್ರ<br />
				ಪ್ರತಿದಿನ ಸಂಜೆ ಗಮಕ ಮೊದಲಾದ ಸಾಂಸ್ಕೃತಿಕ ಕಾರ್ಯಕ್ರಮಗಳನ್ನೂ ಆಯೋಜಿಸಲಾಗಿದೆ.<br /><br />
				ಉಪನ್ಯಾಸಕರು :	ವೇ|| ಬ್ರ || ಶ್ರೀ|| ಅಶ್ವತ್ಥನಾರಾಯಣ ಅವಧಾನಿಗಳು, ವೇ|| ಬ್ರ || ಶ್ರೀ|| ಕೇಶವ ಅವಧಾನಿಗಳು<br />
				ಶಿಬಿರ ಶುಲ್ಕ  :	750 ರೂ.<br />
				ಸಂಪರ್ಕಿಸಿ : 	08182-237724, 9448836895 (ವೇ|| ಬ್ರ || ಶ್ರೀ|| ಕೇಶವ ಅವಧಾನಿಗಳು)<br /><br />

				60 ಜನರಿಗೆ ಮಾತ್ರ ಅವಕಾಶವಿದೆ ಎಂದು ಸಂಘಟಕರು ತಿಳಿಸಿದ್ದಾರೆ. ಆದ್ದರಿಂದ ಆಸಕ್ತರು
				ಬೇಗನೇ ತಮ್ಮ ಹೆಸರನ್ನು ನೋಂದಾಯಿಸಿಕೊಳ್ಳಬೇಕೆಂದು ಪ್ರಾರ್ಥನೆ. ಶಿಬಿರಾರ್ಥಿಗಳು
				ತಮಗೆ ಅಗತ್ಯವೆನಿಸಿದ ಟಾರ್ಚ್, ಸೊಳ್ಳೆನಿರೋಧಕ ಸಾಮಗ್ರಿಗಳು, ಬೆಡ್ ಶೀಟ್ ಗಳನ್ನು ತಂದುಕೊಳ್ಳುವದು.<br /><br />
				(ವಿ. ಸೂ.- ಶಿಬಿರದ ಯಾವದಾದರೊಂದು ದಿನ ಸುತ್ತಮುತ್ತಲಿನ ವೀಕ್ಷಣೀಯಸ್ಥಳಗಳ
				ಕಿರುಪ್ರವಾಸವನ್ನೂ ಆಯೋಜಿಸಲಾಗಿದೆ. ಅದಕ್ಕೆ 100 ರೂ ಶುಲ್ಕವನ್ನು ಇಡಲಾಗಿದೆ)<br /><br />

				ಪೂಜ್ಯ ಶ್ರೀ ಶ್ರೀ ಪ್ರಕಾಶಾನಂದೇಂದ್ರ ಸರಸ್ವತೀ ಸ್ವಾಮಿಗಳವರು 04-09-2009ರಂದು
				ತಮ್ಮ ಚಾತುರ್ಮಾಸ್ಯ ವ್ರತವನ್ನು ಪೂರೈಸಿ ಸೀಮೋಲ್ಲಂಘನಕ್ಕಾಗಿ ಬೆಂಗಳೂರಿನ ಕಾರ್ಯಾಲಯಕ್ಕೆ
				ತೆರಳಿದ್ದಾರೆ ಮತ್ತು 15 ದಿನಗಳ ಕಾಲ ಪ್ರವಚನಗಳನ್ನು ನಡೆಸಿಕೊಡಲಿದ್ದಾರೆ.
				ಸ್ಥಳೀಯ ಜಿಜ್ಞಾಸುಗಳು ಈ ಲಾಭವನ್ನು ಪಡೆಯಬಹುದು.<br /><br />
 
				<span class="bld" style="text-decoration:underline;">ಶ್ರೀದಿಗ್ವಿಜಯ ರಾಮದೇವರಿಗೆ ರಜತ ಕವಚ ಸಮರ್ಪಣೆ - ಭಕ್ತರಿಂದ</span><br />
				(ಪ್ರಾರ್ಥನೆ ಸೀತಾಲಕ್ಷ್ಮಣಹನುಮತ್ ಸಮೇತರಾದ ಶ್ರೀದಿಗ್ವಿಜಯರಾಮಚಂದ್ರದೇವರಿಗೆ ಬೆಳ್ಳಿಯ ಕವಚ
				ಮಾಡಿಸಬೇಕೆಂಬ ಭಕ್ತಮಹಾಶಯರುಗಳ ಅಪೇಕ್ಷೆಯಂತೆ ಈಗ ಒಂದು ಲಕ್ಷಕ್ಕೂ ಹೆಚ್ಚು ಹಣ ಸಂಗ್ರಹವಾಗಿದೆ.
				ಆದರೆ ಅಂದಾಜು ವೆಚ್ಚ ಎರಡು ಲಕ್ಷವಾಗುವದರಿಂದ ಭಕ್ತಾದಿಗಳು ಉದಾರವಾಗಿ ಧನಸಹಾಯವನ್ನು ಮಾಡಬೇಕೆಂದು ಪ್ರಾರ್ಥಿಸಿಕೊಳ್ಳುತ್ತೇವೆ .)<br /><br />
 
				ಶ್ರೀ ಶ್ರೀಗಳವರು ಸ್ಥಾಪಿಸಿದ ಶ್ರೀದಿಗ್ವಿಜಯ ಜಟಾಧಾರಿ ಶ್ರೀರಾಮಚಂದ್ರನಿಗೆ ಮತ್ತು ಸೀತಾ, ಲಕ್ಷ್ಮಣಮಾರುತಿಯ ವಿಗ್ರಹಗಳಿಗೆ ಭಕ್ತಮಹಾಶಯರುಗಳಿಂದ 25/10/2009 ರಂದು ಬೆಳ್ಳಿಕವಚ ಸಮರ್ಪಣೆ ಮಾಡಲಾಗುವದು. ಅದಕ್ಕಾಗಿ 24 ಮತ್ತು 25ರಂದು ವಿಶೇಷಕಾರ್ಯಕ್ರಮಗಳು ನಡೆಯಲಿವೆ.

				24/10/2009ರಂದು ಹತ್ತು ಜನ ವೇದವಿದ್ವಾಂಸರಿಂದ ರಾಮಾಯಣ ಪಾರಾಯಣ ಮತ್ತು ಯಜುರ್ವೇದಪಾರಾಯಣಗಳು.
				ಸಂಜೆ: ಕ್ರಮಾರ್ಚನೆ, ಕಲಶಸ್ಥಾಪನೆ, ಉದಕಶಾಂತಿ ಮುಂತಾದ ಕಾರ್ಯಕ್ರಮಗಳು ನಡೆಯುತ್ತವೆ.
				
				25/10/2009ರಂದು ಶ್ರೀದಿಗ್ವಿಜಯ ರಾಮದೇವರಿಗೆ ಕಲಾಹೋಮ, ಧಾತ್ರೀ ಹೋಮ ಮತ್ತು ಶ್ರೀ ಶ್ರೀಗಳವರ ಸಮಾಧಿಮಂದಿರದಲ್ಲಿ ವಿಶೇಷಪೂಜೆ, ನಂತರ ಕವಚ ಸಮರ್ಪಣೆ ನಡೆಯುತ್ತದೆ.
				ಪರಮಾರ್ಥಸಾ ಧನೆಯ ಅಡ್ಡಿಗಳಿಗೆ ರಕ್ಷಾಕವಚದಂತಿರುವ ಶ್ರೀದಿಗ್ವಿಜಯ ರಾಮಚಂದ್ರನ ರಜತಕವಚಸಮರ್ಪಣಾ ಸಮಾರಂಭದಲ್ಲಿ ಎಲ್ಲರೂ ಭಾಗವಹಿಸಬೇಕೆಂದು ನಾವು ಪ್ರಾರ್ಥಿಸುತ್ತೇವೆ. 
