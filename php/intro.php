			<div class="subtitle">ವಿದ್ಯಾರ್ಥಿಗಳಿಗಿರಬೇಕಾದ ಅರ್ಹತೆ</div><br />
			<p>ಈಗಿನ ಜನಜೀವನಕ್ರಮವನ್ನರಿತುಕೊಂಡು ವೈದಿಕ, ಅವೈದಿಕ ಬ್ರಾಹ್ಮಣ, ಅಬ್ರಾಹ್ಮಣ-ಮುಂತಾದ ಭೇದಭಾವನೆಗಳನ್ನು ಮುರಿದು ತಮ್ಮ ಅನುಷ್ಠಾನದಿಂದ ಜನರಿಗೆ ಮೇಲುಙ್ಕ್ತೆಯಾಗಿ ಪರಸ್ಪರ ಸೌಹಾರ್ದವನ್ನೂ ಸಹಾನುಭೂತಿಯನ್ನೂ ಬೆಳೆಯಿಸಬಲ್ಲ ಉದಾರದೃಷ್ಟಿಯ ವೈದಿಕ ಧರ್ಮೋಪದೇಶಕರನ್ನು ಸಜ್ಜುಗೊಳಿಸುವುದೇ ವಿದ್ಯಾಲಯದ ಹೆಗ್ಗುರಿಯು.</p><br />
			<p>ಕಾವ್ಯ/ನಾಟಕದ ಮಟ್ಟದ ಸಂಸ್ಕೃತ ಪರಿಚಯವನ್ನು ಬಲ್ಲವರಾಗಲಿ, ಕಾಲೇಜು ಮಟ್ಟದ ಇಂಗ್ಲೀಷ್ ಪರಿಚಯವನ್ನು ಬಲ್ಲವರಾಗಲಿ ವಿದ್ಯಾಲಯಕ್ಕೆ ಸೇರಬಹುದು. ಇಂಗ್ಲೀಷ್ ಪರಿಚಯವಿಲ್ಲದ ಸಂಸ್ಕೃತ ವಿದ್ಯಾರ್ಥಿಗಳಿಗೂ, ಸಂಸ್ಕೃತ ಪರಿಚಯವಿಲ್ಲದ ಇಂಗ್ಲೀಷ್/ಕನ್ನಡ/ತೆಲುಗು ವಿದ್ಯಾರ್ಥಿಗಳಿಗೂ ಆಯಾ ಭಾಷೆಗಳನ್ನು ಕಲಿಸಿಕೊಡಲಾಗುವುದು.</p><br />
			<p>ವಾಚಾಲತೆ, ಸಂಸ್ಕೃತಜ್ಞಾನ, ಗ್ರಹಣಶಕ್ತಿ-ಇವುಗಳು ಹೆಚ್ಚಾಗಿರುವವರನ್ನು ಮೊದಲು ಆರಿಸಿಕೊಳ್ಳಲಾಗುವುದು. ಒಂದೆರಡು ತಿಂಗಳು ಪರೀಕ್ಷೆಯನ್ನು ಮಾಡಿದ ಮೇಲೆಯೇ ನಿಯತವಿದ್ಯಾರ್ಥಿಗಳ ಗುಂಪಿಗೆ ಸೇರಿಸಿಕೊಳ್ಳಲಾಗುತ್ತದೆ. </p><br />
			<p>ಸಾಮಾನ್ಯವಾಗಿ ಪಾಠಗಳು ಸಂಸ್ಕೃತ/ಇಂಗ್ಲೀಷ್ ಮಾಧ್ಯಮದಲ್ಲಿಯೇ ಇರುತ್ತವೆ. ಅರ್ಹತೆ ಇರುವ ವಿದ್ಯಾರ್ಥಿಗಳಿಗೆ ಉಚಿತವಾಗಿ ಊಟ ಮತ್ತು ವಸತಿಗಳ ಏರ್ಪಾಡು ಮಾಡಲಾಗುವುದು. </p><br />
			<p>ಹತ್ತರಿಂದ ಹದಿನಾರು ವರ್ಷ ವಯೋಮಿತಿ ಇರುವ ಕಿರಿಯ ವಿದ್ಯಾರ್ಥಿಗಳಿಗೆ ಸಂಸ್ಕೃತಭಾಷಾಭ್ಯಾಸ, ಸೂಕ್ತ ವೇದಾಧ್ಯಯನ, ಪೂಜಾವಿಧಾನ ಇತ್ಯಾದಿಗಳನ್ನು ಹೇಳಿಕೊಡುವ ಏರ್ಪಾಡನ್ನೂ ಮಾಡಲಾಗಿದೆ. ಈ ಅಭ್ಯಾಸಕ್ರಮಕ್ಕೆ ಮೂರು ವರ್ಷಗಳ ಕಾಲ ನಿಗದಿ ಪಡಿಸಲಾಗಿದೆ. ಈ ಏರ್ಪಾಡು ಐದು ವಿದ್ಯಾರ್ಥಿಗಳಿಗೆ ಸೀಮಿತ.</p><br />
			<p>ಮೇಲೆ ವಿವರಿಸಿರುವಂತೆ ಅರ್ಹತೆಯುಳ್ಳವರು ಏಪ್ರಿಲ್ ೧೫ನೇ ದಿನಾಂಕದ ಒಳಗೆ ಅಧಿಕೃತ ಪ್ರವೇಶಪತ್ರ, ಅಭ್ಯಾಸಕ್ರಮ ಇತ್ಯಾದಿಗಳನ್ನು ಈ ಕೆಳಕಂಡ ವಿಳಾಸದಿಂದ ಪಡೆದು ಪ್ರವೇಶಪತ್ರವನ್ನು ಸಲ್ಲಿಸಬೇಕು. ಪಾಠಗಳು ಮೇ ೧ನೇ ದಿನಾಂಕದಿಂದ ಪ್ರಾರಂಭವಾಗಲಿದೆ.</p><br />
