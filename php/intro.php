			<p>ಪ್ರಿಯ ಅಧ್ಯಾತ್ಮ ಪ್ರೇಮಿಗಳೇ,</p><br />	
			<p>ಅಧ್ಯಾತ್ಮಪ್ರಕಾಶ ಕಾರ್ಯಾಲಯವು ಅಧ್ಯಾತ್ಮ ವಿದ್ಯೆಯಲ್ಲಿ ಜನರಿಗೆ ಹೆಚ್ಚು ಆದರವನ್ನುಂಟು ಮಾಡಬೇಕೆಂಬ ಉದ್ದೇಶದಿಂದ ೧೯೨೦ನೆಯ ಇಸವಿಯಿಂದಲೂ ದುಡಿಯುತ್ತಿದೆಯೆಂಬುದನ್ನೂ ಕನ್ನಡ, ಸಂಸ್ಕೃತ, ಇಂಗ್ಲೀಷು ಭಾಷೆಗಳಲ್ಲಿ ಬರೆದಿರುವ ಅಧ್ಯಾತ್ಮಗ್ರಂಥಗಳನ್ನು ಪ್ರಕಟಿಸುವುದು. &ldquo;ಅಧ್ಯಾತ್ಮಪ್ರಕಾಶ&rdquo; ವೆಂಬ ಮಾಸಪತ್ರಿಕೆಯನ್ನು ಹೊರಡಿಸುವುದು; ಗ್ರಂಥಕಾಲಕ್ಷೇಪಗಳನ್ನು ಏರ್ಪಡಿಸುವದು; ಅಧ್ಯಾತ್ಮ ಸಂಬಂಧವಾದ ಉತ್ಸವಗಳನ್ನೂ ಉಪನ್ಯಾಸಗಳನ್ನೂ ಏರ್ಪಡಿಸುವುದು- ಈ ಬಗೆಯ ಸಾಧನಗಳಿಂದ ಅಧ್ಯಾತ್ಮವಿದ್ಯಾಪ್ರಸಾರವನ್ನು ಮಾಡುತ್ತಿದೆಯೆಂಬುದನ್ನೂ ಈಗ ಕನ್ನಡನಾಡಿನಲ್ಲಿಯೂ ಮತ್ತು ಇತರ ಪ್ರಾಂತಗಳಲ್ಲಿಯೂ ಅನೇಕರು ಬಲ್ಲವರಾಗಿದ್ದಾರೆ. ಅಧ್ಯಾತ್ಮಪ್ರಕಾಶ ಕಾರ್ಯಾಲಯದ ಅಂಗವಾಗಿರುವ ವಿದ್ಯಾಲಯದಲ್ಲಿ ಅಧ್ಯಾತ್ಮ ವಿದ್ಯಾ ಪ್ರಚಾರಕರನ್ನು ಸಜ್ಜುಗೊಳಿಸಲು ಮತತ್ರಯಗಳಿಗೊಪ್ಪಾಗುವ ಒಂದು ಅಭ್ಯಾಸ ಪದ್ಥತಿಯನ್ನೂ ಏರ್ಪಡಿಸಿರುತ್ತದೆ. ಶ್ರೀ ಶ್ರೀ ಪ್ರಕಾಶಾನಂದೇಂದ್ರಸರಸ್ವತೀಸ್ವಾಮಿಗಳ ನೇತೃತ್ವದಲ್ಲಿ ಈ ಅಭ್ಯಾಸ ಪದ್ಥತಿಯಲ್ಲಿ ಈ ವರ್ಷವೂ ಕೂಡ ಸಂಸ್ಕತ, ಇಂಗ್ಲೀಷು, ಕನ್ನಡ- ಈ ಮೂರು ಭಾಷೆಗಳಲ್ಲಿರುವ ಗ್ರಂಥಗಳ ಮೂಲಕ ತತ್ತ್ವವಿಚಾರವನ್ನೂ ಸಾಧನಕ್ರಮವನ್ನೂ ತಿಳಿಸಿಕೊಡಲಾಗುವುದು. ವಿದ್ಯಾಲಯದ ವ್ಯಾಪ್ತಿಯು ಇಡಿ ಭಾರತ ದೇಶಕ್ಕೆ ಇರುವುದು. ಆದ್ದರಿಂದ ಬೋಧನಕ್ರಮದಲ್ಲಿ ಉಪಯೋಗಿಸುವ ಪ್ರಧಾನಭಾಷೆಗಳು ಸಂಸ್ಕೃತ ಮತ್ತು ಇಂಗ್ಲೀಷ್ ಆಗಿರುವುದು.</p><br />
			<div class="subtitle">ಅಭ್ಯಾಸದ ವಿಷಯಗಳು</div><br />
			<ul>
				<li>ಉಪನಿಷತ್ತುಗಳು, ಭಗವದ್ಗೀತೆ, ಬ್ರಹ್ಮಸೂತ್ರ-ಎಂಬ ಪ್ರಸ್ಥಾನತ್ರಯವನ್ನು ದಕ್ಷಿಣದೇಶದಲ್ಲಿ ಪ್ರಚಾರದಲ್ಲಿರುವ ಮತತ್ರಯದ ಪರಿಚಯದೊಡನೆ ಹೇಳಿಕೊಡಲಾಗುತ್ತದೆ. ಇದರ ಜೊತೆಗೆ ಹಿಂದೂ ಮತದ ಇತಿಹಾಸವನ್ನೂ ಭರತಖಂಡದಲ್ಲಿರುವ ವೀರಶೈವ; ಆರ್ಯಸಮಾಜ; ಬ್ರಹ್ಮಸಮಾಜ-ಮುಂತಾದ ಪಂಥಗಳ ಸಂಕ್ಷಿಪ್ತ ಪರಿಚಯವನ್ನೂ ಸೇರಿಸಿರುತ್ತದೆ. </li>
				<li>ಅವೈದಿಕಮತಗಳ- ಎಂದರೆ ಜೈನ, ಬೌದ್ಧ, ಕ್ರಿಸ್ತಿಯನ್, ಇಸ್ಲಾಂ, ಪಾರ್ಸಿ, ಯಹೋದಿ ಮುಂತಾದವುಗಳ- ಸ್ಥಾಪಕರ ಜೀವನಚರಿತ್ರೆಯನ್ನೂ ಮುಖ್ಯೋಪದೇಶಗಳನ್ನೂ ಇಂಗ್ಲೀಷಿನಲ್ಲಿ ಅಥವಾ ಭಾಷಾಂತರದಲ್ಲಿ ಪ್ರಕಟವಾಗಿರುವ ಪುಸ್ತಕಗಳ ಸಹಾಯದಿಂದ ತಿಳಿಸಿಕೊಡಲಾಗುತ್ತದೆ.</li>
				<li>ಜಪ, ಪೂಜೆ, ಧ್ಯಾನ-ಮುಂತಾದ ಸಾಧನಗಳ ಮರ್ಮವನ್ನು ವೈದಿಕ ಪದ್ಧತಿಗನುಗುಣವಾಗಿ ತಿಳಿಸಿಕೊಡಲಾಗುತ್ತದೆ.</li>
				<li>ಪುರಾಣಪ್ರವಚನ, ಕೀರ್ತನ, ಉಪನ್ಯಾಸ- ಮುಂತಾದ ರೀತಿಯಿಂದ ಅಧ್ಯಾತ್ಮಜೀವನವನ್ನು ನಡೆಯಿಸಿಕೊಂಡು ಸತ್ಕಾಲಕ್ಷೇಪ ಮಾಡುವ ರೀತಿಯನ್ನೂ ಅ ಮೂಲಕ ಜನತಾಸೇವೆಯನ್ನು ಮಾಡುವ ಕ್ರಮವನ್ನೂ ತಿಳಿಸಿಕೊಡಲಾಗುತ್ತದೆ.</li>
